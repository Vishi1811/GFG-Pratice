class Solution{
public:
    int isPrime(int N){
        // code here
        if (N <= 1) {
            return false;
        }
        if (N <= 3) {
            return true;
        }
        if (N % 2 == 0 || N % 3 == 0) {
            return false;
        }
        int i = 5;
        while (i * i <= N) {
            if (N % i == 0 || N % (i + 2) == 0) {
                return false;
            }
            i += 6;
        }
        return true;
       
    }
};


