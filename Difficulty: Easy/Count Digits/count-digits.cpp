class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int count=0;
        int num=N;
        while(num>0)
        {
            int x=num%10;
             if (x != 0 && N % x == 0)
            {
               count++; 
            }
            num=num/10;
        }
        return count;
    }
};

